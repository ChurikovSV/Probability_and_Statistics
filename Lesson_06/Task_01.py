# Известно, что генеральная совокупность распределена нормально со средним
# квадратическим отклонением, равным 16. Найти доверительный интервал для
# оценки математического ожидания a с надежностью 0.95, если выборочная
# средняя M = 80, а объем выборки n = 256.


import scipy.stats as stats
import math

# Данные задачи
M = 80
std = 16
n = 256
a = 0.95

# Квантиль нормального распределения для заданного уровня надежности
z = stats.norm.ppf((1 + a) / 2)

# Стандартная ошибка среднего
se = std / math.sqrt(n)

# Границы доверительного интервала
lower_bound = M - z * se
upper_bound = M + z * se

# Результат
print(f"Доверительный интервал: ({lower_bound:.2f}, {upper_bound:.2f})")