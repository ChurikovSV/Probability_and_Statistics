# Проведите тест гипотезы. Утверждается, что шарики для подшипников,
# изготовленные автоматическим станком, имеют средний диаметр 17 мм.
# Используя односторонний критерий с α=0,05, проверить эту гипотезу, если в
# выборке из n=100 шариков средний диаметр оказался равным 17.5 мм, а
# дисперсия известна и равна 4 кв. мм.

import scipy.stats as stats

# Данные
mean_sample = 17.5
mean_hypothesis = 17
variance = 4
std_dev = variance ** 0.5
n = 100
alpha = 0.05

# Вычисление Z-статистики
z_statistic = (mean_sample - mean_hypothesis) / (std_dev / n ** 0.5)

# Критическое значение Z для одностороннего теста
z_critical = stats.norm.ppf(1 - alpha)

# Вывод результатов
print(f"Z-статистика: {z_statistic:.2f}")
print(f"Критическое значение Z: {z_critical:.2f}")

if z_statistic > z_critical:
    print("Отклоняем нулевую гипотезу: средний диаметр шариков больше 17 мм.")
else:
    print("Не отклоняем нулевую гипотезу: средний диаметр шариков не больше 17 мм.")


# Z-статистика: 2.50
# Критическое значение Z: 1.64
